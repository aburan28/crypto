//! **Camellia** — 128-bit block cipher (Aoki et al., NTT & Mitsubishi
//! 2000), standardised in **RFC 3713** and **ISO/IEC 18033-3**.
//!
//! Camellia is a Feistel-style cipher offering security comparable to
//! AES.  It is one of two algorithms (alongside AES) listed in the
//! Japanese e-Government CRYPTREC and EU NESSIE recommended-cipher
//! portfolios.
//!
//! ## Variants implemented
//!
//! | Name           | Block | Key  | Rounds | FL layers |
//! |----------------|-------|------|--------|-----------|
//! | [`Camellia128`]| 128 b | 128b | 18     | after r6, r12 |
//! | [`Camellia192`]| 128 b | 192b | 24     | after r6, r12, r18 |
//! | [`Camellia256`]| 128 b | 256b | 24     | after r6, r12, r18 |
//!
//! ## Round function
//!
//! Each Feistel round on a 128-bit state `(L, R)` (two 64-bit halves)
//! with round key `k`:
//!
//! ```text
//!     (L, R) ← (R ⊕ F(L, k), L)
//! ```
//!
//! `F(x, k) = P(S(x ⊕ k))` where `S` applies eight 8-bit S-boxes (the
//! four variants s1, s2, s3, s4 = s1∘rot, s1<<1, s1>>1) and `P` is a
//! GF(2) linear layer mixing the 8 output bytes.
//!
//! ## FL / FL⁻¹ keyed linear layers
//!
//! Inserted between every six rounds.  On a 64-bit input `x` with a
//! 64-bit subkey `(kl, kr)` (high and low 32-bit halves):
//!
//! ```text
//!     FL(x, k):    xR ⊕= ROL_1(xL ∧ kl);  xL ⊕= xR ∨ kr
//!     FL⁻¹(x, k):  xL ⊕= xR ∨ kr;        xR ⊕= ROL_1(xL ∧ kl)
//! ```
//!
//! ## Key schedule
//!
//! The user key is expanded into KL (the user key padded/folded) and
//! KR.  An auxiliary 128-bit value KA is generated by four Feistel
//! rounds applied to `KL ⊕ KR` using the Sigma constants as round
//! keys; for 192/256-bit keys a second auxiliary value KB is generated
//! from `KA ⊕ KR` by two more rounds.  Round keys, FL keys, and
//! whitening keys are produced by 128-bit left-rotations of KL, KR,
//! KA, KB at the rotation amounts specified in RFC 3713 §2.4.
//!
//! ## Byte conventions
//!
//! Camellia is a **big-endian** spec: bytes are loaded MSB-first into
//! 64-bit halves.  The 128-bit block is `bytes[0..8] = L (high)` and
//! `bytes[8..16] = R (low)`.

