//! **SEED** — 128-bit block cipher, 128-bit key (KISA, 1998),
//! Korean national standard TTAS.KO-12.0004, specified in
//! **RFC 4269** (2005).
//!
//! ## Structure
//!
//! - 16-round balanced Feistel on a 128-bit block split into two
//!   64-bit halves `(L, R)`.
//! - Round function `F(R, K_i) = G(G(R ⊕ K_{i,1}) ⊕ R[high half] ⊕
//!   K_{i,0})` — actually a small mixing function over two 32-bit
//!   words; see [`f_function`] for the exact structure.
//! - `G` is a non-linear function applying two parallel S-boxes
//!   `SS0..SS3` (combining `S1`, `S2` with linear masking constants
//!   `m0..m3`) to four input bytes, mixing via XOR.
//!
//! ## Key schedule
//!
//! 16 rounds × 2 32-bit subkeys per round = 32 subkeys.  The 128-bit
//! key is split into four 32-bit words `A, B, C, D`; round keys are
//! generated by `G`-mixing `(A ± B ± C ± D ± KC_i)` with the round
//! constants `KC_i` derived from the golden-ratio fractional bits.
//! Between rounds the `(A, B)` and `(C, D)` pairs are alternately
//! rotated by 8 bits.
//!
//! ## References
//!
//! - **RFC 4269** — The SEED Encryption Algorithm.

// ── S-boxes SS0..SS3 (RFC 4269 §A) ───────────────────────────────────
//
// SS0..SS3 incorporate the S1/S2 lookups with the m0..m3 linear masks
// already applied, so the G function can XOR them directly.

