// SS0[x] places `S1(x) & M0` at byte 0, `S1(x) & M1` at byte 1,
// `S1(x) & M2` at byte 2, `S1(x) & M3` at byte 3 (LSB-first).
// (Equivalent constructions appear in OpenSSL & RFC reference code.)
const SS0_TABLE: [u32; 256] = {
    let mut t = [0u32; 256];
    let mut i = 0;
    while i < 256 {
        let s = S1_TABLE[i];
        let b0 = (s & M0) as u32;
        let b1 = ((s & M1) as u32) << 8;
        let b2 = ((s & M2) as u32) << 16;
        let b3 = ((s & M3) as u32) << 24;
        t[i] = b0 | b1 | b2 | b3;
        i += 1;
    }
    t
};

