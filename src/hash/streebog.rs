//! **Streebog** — Russian national hash standard GOST R 34.11-2012.
//! Replaces the deprecated GOST R 34.11-94.  Two output sizes:
//! 256-bit (`Streebog-256`) and 512-bit (`Streebog-512`).  Same
//! compression function for both; output truncation only.
//!
//! ## Structure
//!
//! - Block size: 512 bits (64 bytes).
//! - Compression function `g_N`: encrypts the input block under a
//!   key derived from the chaining value `h` and counter `N`,
//!   then XORs the result with `h` and the block.
//! - 12-round AES-like cipher inside `g_N` using the Streebog
//!   S-box `pi` and a permutation `P`.
//! - Final block padding: append `0x01` after the message bits,
//!   pad with zeros to 512 bits if needed.
//! - Output: `h ⊕ G(N, h, length_bits ⊕ counter) ⊕ G(0, h_final, Σ)`
//!   (final mixing with bit-count and message-sum).

