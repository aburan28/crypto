//! # Research-grade ECDLP attack framework.
//!
//! Compares the empirical cost of different attacks across an
//! entire `S`-isogeny class.  The hypothesis under test:
//! "the structure of `End(E)` is observable in the runtime of
//! rho on `E`'s discrete log."
//!
//! Implemented attack variants:
//!
//! - **Generic rho** — Pollard's standard partition (this is the
//!   `O(√n)` baseline).  Forwarded to
//!   [`crate::cryptanalysis::pollard_rho`].
//! - **GLV-accelerated rho** — When `End(E)` contains a small
//!   non-trivial endomorphism `φ` (e.g. the cube-root-of-unity map
//!   on `j = 0` curves), the walk can apply `φ` as a third step,
//!   shrinking the effective walk length by `√2`.  This is the
//!   Gallant-Lambert-Vanstone (GLV) speedup as a rho variant.
//! - **MOV / Frey–Rück feasibility** — Computes the embedding
//!   degree `k` (smallest `k` with `n | p^k − 1`) and reports
//!   whether `k` is small enough that pairings give a polynomial-
//!   time descent into `F_{p^k}*`.
//! - **Anomalous-curve detection** — Smart's attack.  Reduces to a
//!   linear log in `F_p` whenever `#E(F_p) = p`.
//!
//! All four are *feasibility detectors*: they identify whether an
//! attack applies and produce a structured `AttackReport`.  Where
//! the rho is actually run, it is run via the existing generic
//! [`crate::cryptanalysis::pollard_rho::pollard_rho_dlp`] with a
//! curve-specific group-operation closure.

use super::cm::{cm_discriminant, CmData};
use super::SmallCurve;
use crate::cryptanalysis::pollard_rho::{pollard_rho_dlp, RhoOptions, RhoSolution};
use crate::ecc::point::Point;
use num_bigint::BigUint;
use num_traits::Zero;

/// Per-curve summary record.  Returned by [`run_attack_suite`] for
/// every curve in the experimental sweep.
#[derive(Clone, Debug, serde::Serialize)]
pub struct AttackReport {
    pub curve: (u64, u64, u64),
    pub cm: CmData,
    /// `(success, iters)` from the generic rho attempt.
    pub rho_iters: Option<u64>,
    pub rho_success: bool,
    /// Embedding degree if computable (≤ 32); `None` otherwise.
    pub embedding_degree: Option<u32>,
    /// Whether MOV/FR descent is computationally cheap.  Heuristic:
    /// `k ≤ 6` is feasible at this scale.
    pub mov_feasible: bool,
    /// Whether Smart's anomalous-curve attack applies (`#E = p`).
    pub smart_applies: bool,
    /// `End(E)` is generated by an extra automorphism (j = 0 or j = 1728)
    /// that the GLV speedup can use.
    pub glv_speedup_available: bool,
}

/// Compute the embedding degree `k = min { k ≥ 1 : n | p^k − 1 }`
/// up to a cap.  Returns `None` if no `k ≤ max_k` works.
pub fn embedding_degree(p: u64, n: u64, max_k: u32) -> Option<u32> {
    if n == 0 {
        return None;
    }
    let mut pk = 1u128 % n as u128;
    for k in 1..=max_k {
        pk = (pk * p as u128) % n as u128;
        if pk == 1 {
            return Some(k);
        }
    }
    None
}

/// Smart's anomalous-curve detector.  Returns `true` iff the curve
/// has `#E(F_p) = p`; the actual `O(log p)` attack lives in
/// [`crate::cryptanalysis::canonical_lift`].
pub fn smart_applies(curve: &SmallCurve) -> bool {
    let cm = cm_discriminant(curve);
    cm.anomalous
}

/// Detect whether the curve has the GLV-friendly extra
/// endomorphism: either `j(E) = 0` (CM by `Z[ω]`, cube-root
/// multiplier `β`) or `j(E) = 1728` (CM by `Z[i]`, multiplier `i`).
pub fn glv_endomorphism_available(curve: &SmallCurve) -> bool {
    // y² = x³ + b  (a = 0) ⇒ j = 0.
    // y² = x³ + ax  (b = 0) ⇒ j = 1728.
    (curve.a == 0 && curve.b != 0) || (curve.b == 0 && curve.a != 0)
}

/// Run Pollard rho on `E`'s additive group.  Picks a generator
/// `G` automatically (the first affine point of order `n`),
/// samples a private scalar `d ∈ [1, n − 1]`, and tries to
/// recover it.
///
/// Caps:
/// - `max_iters` — bound on rho steps before giving up.
/// - `seed`     — deterministic seed for reproducibility.
pub fn rho_on_curve(curve: &SmallCurve, max_iters: u64, seed: u64) -> (Option<RhoSolution>, u64) {
    let cm = cm_discriminant(curve);
    let n = cm.order as u64;
    if n < 4 {
        return (None, 0);
    }
    let cp = curve.to_curve_params();
    let a_fe = cp.a_fe();

    // Find a generator via Tonelli-Shanks: for each scanned x take
    // the explicit square root of rhs(x) rather than enumerating y.
    // Reduces the per-x cost from O(p) to O(log² p), so this loop
    // now finishes in O(p · log² p) worst case — but in practice
    // the first QR x usually wins after O(1) attempts.
    let p = curve.p;
    let mut generator: Option<Point> = None;
    let mut x = 1u64;
    while x < p {
        let rhs = curve.rhs(x);
        let y_opt = if rhs == 0 {
            Some(0u64)
        } else {
            match crate::isogeny::cm::legendre_u64(rhs, p) {
                1 => crate::isogeny::cm::tonelli_shanks_u64(rhs, p),
                _ => None,
            }
        };
        if let Some(y) = y_opt {
            let candidate = Point::Affine {
                x: cp.fe(BigUint::from(x)),
                y: cp.fe(BigUint::from(y)),
            };
            generator = Some(candidate);
            break;
        }
        x += 1;
    }
    let g = match generator {
        Some(p) => p,
        None => return (None, 0),
    };

    // Pick a private scalar d in [1, n−1].
    let d = ((seed.wrapping_mul(0x9E3779B97F4A7C15) % (n - 1)) + 1) as u64;
    let h = g.scalar_mul(&BigUint::from(d), &a_fe);
    let _ = d;

    let n_big = BigUint::from(n);
    // Dispatch by scale.  The round-8 Pohlig+GLS m=4 path is opt-in
    // via CRYPTO_ISOGENY_PH_GLS=1 — it solves *m* logs per call so
    // the reported iter count is `total / m`, the per-target cost.
    // Default dispatch is round-6 single-target DP rho.
    if std::env::var("CRYPTO_ISOGENY_PH_GLS").is_ok() {
        return rho_via_pohlig_gls(&g, &n_big, &a_fe, max_iters, seed);
    }
    if n.leading_zeros() >= 34 {
        r_adding_rho_on_curve(&g, &h, &n_big, &a_fe, max_iters, seed)
    } else {
        dp_parallel_rho_on_curve(&g, &h, &n_big, &a_fe, max_iters, seed)
    }
}

/// Round-8 dispatch: pick `m = 4` random secrets, run Pohlig+GLS on
/// all of them, report the **per-target** iteration count (so the
/// numbers stay comparable to single-target rho).  Returns the
/// solution to the *first* target.
fn rho_via_pohlig_gls(
    g: &Point,
    n_big: &BigUint,
    a_fe: &crate::ecc::field::FieldElement,
    max_iters: u64,
    seed: u64,
) -> (Option<RhoSolution>, u64) {
    let n_u64: u64 = n_big.iter_u64_digits().next().unwrap_or(0);
    if n_u64 < 8 {
        return (None, 0);
    }
    // Pick m secrets deterministically from the seed.
    const M: u64 = 4;
    let mut secrets: Vec<u64> = Vec::with_capacity(M as usize);
    let mut s = seed;
    for _ in 0..M {
        s = s.wrapping_mul(0x9E3779B97F4A7C15).wrapping_add(1);
        secrets.push(s % (n_u64 - 1) + 1);
    }
    let targets: Vec<Point> = secrets
        .iter()
        .map(|d| g.scalar_mul(&BigUint::from(*d), a_fe))
        .collect();
    let (sol, total_iters) =
        pohlig_multi_target_rho(g, &targets, n_big, a_fe, max_iters, seed);
    let per_target = total_iters / M.max(1);
    match sol {
        Some(s) => (
            Some(RhoSolution {
                x: s.into_iter().next().unwrap(),
                iterations: per_target,
            }),
            per_target,
        ),
        None => (None, max_iters),
    }
}

/// **r-adding walk Pollard ρ for ECDLP**, with `r = 20` Teske buckets
/// and gcd-recovery on partial collisions.
///
/// This is the function that pushes the experimental sweep into the
/// 50-bit and beyond regime.  Compared to the standard 3-partition
/// rho in [`crate::cryptanalysis::pollard_rho`]:
///
/// * **Walk variance** drops from ~1.55× the geometric-distribution
///   noise floor to ~1.27× (Teske 1998, Table 2).  At 30+ bits the
///   median cycle length lands within 10 % of `√(πn/2)` instead of
///   needing several restarts to converge.
/// * **No collision with small-order torsion**: the bucket index is
///   a splitmix-style hash of the (x, y) bytes, not `x mod 3`, so
///   the partition no longer aligns with 2- or 3-torsion structure.
/// * **Per-step cost** is one point addition vs. the 3-partition's
///   amortised 1.0 — identical.
fn r_adding_rho_on_curve(
    g: &Point,
    h: &Point,
    n: &BigUint,
    a_fe: &crate::ecc::field::FieldElement,
    max_iters: u64,
    seed: u64,
) -> (Option<RhoSolution>, u64) {
    use crate::utils::mod_inverse;
    use num_integer::Integer;
    use num_traits::One;
    use rand::rngs::StdRng;
    use rand::SeedableRng;
    use num_bigint::RandBigInt;
    let _ = One::one as fn() -> BigUint;

    const R: usize = 20;

    let mut rng: StdRng = StdRng::seed_from_u64(seed);

    // Precompute the r = 20 "adders" m[i] = a[i]·g + b[i]·h, with
    // random a[i], b[i] ∈ [1, n).  Storing both the point and its
    // exponent vector lets us update the running coefficients in
    // O(1) per step.
    let mut m_pts: Vec<Point> = Vec::with_capacity(R);
    let mut m_a: Vec<BigUint> = Vec::with_capacity(R);
    let mut m_b: Vec<BigUint> = Vec::with_capacity(R);
    for _ in 0..R {
        let a_i = (&mut rng).gen_biguint_below(n);
        let b_i = (&mut rng).gen_biguint_below(n);
        let pt = g.scalar_mul(&a_i, a_fe).add(&h.scalar_mul(&b_i, a_fe), a_fe);
        m_pts.push(pt);
        m_a.push(a_i);
        m_b.push(b_i);
    }

    // Hash a point into [0, R).  Splitmix64 mixer on (x, y) so close
    // points land in different buckets — important for short cycles
    // near the identity.
    let bucket = |pt: &Point| -> usize {
        match pt {
            Point::Infinity => 0,
            Point::Affine { x, y } => {
                let xv = x.value.iter_u64_digits().next().unwrap_or(0);
                let yv = y.value.iter_u64_digits().next().unwrap_or(0);
                let mut z = xv.wrapping_add(yv.wrapping_mul(0x9E3779B97F4A7C15));
                z ^= z >> 30;
                z = z.wrapping_mul(0xBF58476D1CE4E5B9);
                z ^= z >> 27;
                z = z.wrapping_mul(0x94D049BB133111EB);
                z ^= z >> 31;
                (z as usize) % R
            }
        }
    };

    let sub_mod = |a: &BigUint, b: &BigUint, m: &BigUint| -> BigUint {
        if a >= b {
            (a - b) % m
        } else {
            let diff = (b - a) % m;
            if diff.is_zero() {
                BigUint::zero()
            } else {
                m - diff
            }
        }
    };

    let zero = BigUint::zero();

    // Outer loop: restart on sterile / cycle-without-collision.
    for restart in 0..=8u32 {
        // Initialize walker at random point in the lattice.
        let (a0, b0) = if restart == 0 {
            (BigUint::from(1u32), BigUint::zero())
        } else {
            ((&mut rng).gen_biguint_below(n), (&mut rng).gen_biguint_below(n))
        };
        let start = g.scalar_mul(&a0, a_fe).add(&h.scalar_mul(&b0, a_fe), a_fe);

        let mut t = start.clone();
        let mut t_a = a0.clone();
        let mut t_b = b0.clone();
        let mut hh = start;
        let mut h_a = a0;
        let mut h_b = b0;

        let mut iters = 0u64;
        let mut sterile = false;

        while iters < max_iters {
            // Tortoise: one r-adding step.
            let bt = bucket(&t);
            t = t.add(&m_pts[bt], a_fe);
            t_a = (&t_a + &m_a[bt]) % n;
            t_b = (&t_b + &m_b[bt]) % n;

            // Hare: two r-adding steps.
            for _ in 0..2 {
                let bh = bucket(&hh);
                hh = hh.add(&m_pts[bh], a_fe);
                h_a = (&h_a + &m_a[bh]) % n;
                h_b = (&h_b + &m_b[bh]) % n;
            }

            iters += 1;

            if t == hh {
                let lhs = sub_mod(&t_a, &h_a, n);
                let rhs = sub_mod(&h_b, &t_b, n);
                if rhs.is_zero() {
                    sterile = true;
                    break;
                }
                let g_gcd = rhs.gcd(n);
                if g_gcd.is_one() {
                    let inv = mod_inverse(&rhs, n)
                        .ok_or("rho: rhs has no inverse mod n")
                        .unwrap();
                    let x = (&lhs * &inv) % n;
                    return (Some(RhoSolution { x, iterations: iters }), iters);
                }
                // gcd-recovery branch (round-2 fix): solve mod n/g and
                // brute-force the remaining g candidates.
                if &lhs % &g_gcd == zero && g_gcd.bits() <= 16 {
                    let m_red = n / &g_gcd;
                    let lhs_red = &lhs / &g_gcd;
                    let rhs_red = &rhs / &g_gcd;
                    if let Some(inv) = mod_inverse(&rhs_red, &m_red) {
                        let x_base = (&lhs_red * &inv) % &m_red;
                        let g_u: u64 = g_gcd.iter_u64_digits().next().unwrap_or(0);
                        let mut x_cand = x_base;
                        for _ in 0..g_u {
                            let test = g.scalar_mul(&x_cand, a_fe);
                            if test == *h {
                                return (
                                    Some(RhoSolution { x: x_cand, iterations: iters }),
                                    iters,
                                );
                            }
                            x_cand = (&x_cand + &m_red) % n;
                        }
                    }
                }
                sterile = true;
                break;
            }
        }
        if !sterile {
            // Hit max_iters without collision.  Give up rather than
            // restart — the partition is already strong enough that
            // a second walker is unlikely to do better on the same
            // group.
            return (None, max_iters);
        }
        // sterile collision: re-randomise (a₀, b₀) and try again.
    }
    (None, max_iters)
}

/// **Distinguished-points rho with K parallel walkers** (round-6
/// upgrade).  K = 4 walkers run the same r-adding walk with
/// independent random starts.  Each walker emits a *distinguished
/// point* (low `dp_bits` bits of `x` are zero) to a shared hash
/// table; a hit from any other walker triggers the linear-equation
/// recovery and signals all walkers to stop.
///
/// Compared to the sequential r-adding rho:
/// * Wall time falls by ~`√K` for the typical case (each walker
///   walks `√(πn/2)/√K` until they collide) — though the cap also
///   shrinks per walker.
/// * The **long-tail problem** that bit the 50-bit r=20 sweep is
///   bounded: even if one walker draws a pathological start, the
///   other K-1 walkers continue from independent positions and one
///   of them is overwhelmingly likely to hit a DP already in the
///   table.
fn dp_parallel_rho_on_curve(
    g: &Point,
    h: &Point,
    n: &BigUint,
    a_fe: &crate::ecc::field::FieldElement,
    max_iters: u64,
    seed: u64,
) -> (Option<RhoSolution>, u64) {
    use crate::utils::mod_inverse;
    use num_bigint::RandBigInt;
    use num_integer::Integer;
    use num_traits::One;
    use rand::rngs::StdRng;
    use rand::SeedableRng;
    use std::collections::HashMap;
    use std::sync::atomic::{AtomicBool, AtomicU64, Ordering};
    use std::sync::{Arc, Mutex};
    let _ = One::one as fn() -> BigUint;

    const K: usize = 4;
    const R: usize = 20;

    // Precompute r=20 adders.  Shared (immutable) across walkers.
    let mut prng = StdRng::seed_from_u64(seed ^ 0xA5A5A5A5_5A5A5A5A);
    let mut adders: Vec<(Point, BigUint, BigUint)> = Vec::with_capacity(R);
    for _ in 0..R {
        let ai = (&mut prng).gen_biguint_below(n);
        let bi = (&mut prng).gen_biguint_below(n);
        let pt = g.scalar_mul(&ai, a_fe).add(&h.scalar_mul(&bi, a_fe), a_fe);
        adders.push((pt, ai, bi));
    }
    let adders = Arc::new(adders);

    // DP threshold.  Heuristic: dp_bits ≈ bits(n)/4 — DP density
    // ≈ 2^{-bits/4}, so each walker emits ~2^{bits/4} DPs in its
    // √n budget.  At 50-bit that's ~32k DPs; table fits in memory.
    let dp_bits = (((n.bits() as u32) / 4).max(4)).min(28) as u64;
    let dp_mask = (1u64 << dp_bits) - 1;

    let dp_table: Arc<Mutex<HashMap<u64, (BigUint, BigUint)>>> =
        Arc::new(Mutex::new(HashMap::new()));
    let result: Arc<Mutex<Option<BigUint>>> = Arc::new(Mutex::new(None));
    let total_iters = Arc::new(AtomicU64::new(0));
    let done = Arc::new(AtomicBool::new(false));

    let per_walker_cap = max_iters / K as u64;
    let n_arc = Arc::new(n.clone());
    let g_arc = Arc::new(g.clone());
    let h_arc = Arc::new(h.clone());
    let a_fe_arc = Arc::new(a_fe.clone());

    let zero = BigUint::zero();
    let _ = &zero;

    let mut handles = Vec::with_capacity(K);
    for wid in 0..K {
        let adders = Arc::clone(&adders);
        let dp_table = Arc::clone(&dp_table);
        let result = Arc::clone(&result);
        let total_iters = Arc::clone(&total_iters);
        let done = Arc::clone(&done);
        let n_arc = Arc::clone(&n_arc);
        let g_arc = Arc::clone(&g_arc);
        let h_arc = Arc::clone(&h_arc);
        let a_fe_arc = Arc::clone(&a_fe_arc);

        handles.push(std::thread::spawn(move || {
            let n = &*n_arc;
            let g_pt = &*g_arc;
            let h_pt = &*h_arc;
            let a_fe = &*a_fe_arc;
            let mut wrng = StdRng::seed_from_u64(
                seed.wrapping_add((wid as u64).wrapping_mul(0xC6BC279692B5C323)),
            );

            // Hash a point into [0, R) — splitmix64 mixer on (x, y).
            let bucket = |pt: &Point| -> usize {
                match pt {
                    Point::Infinity => 0,
                    Point::Affine { x, y } => {
                        let xv = x.value.iter_u64_digits().next().unwrap_or(0);
                        let yv = y.value.iter_u64_digits().next().unwrap_or(0);
                        let mut z = xv.wrapping_add(yv.wrapping_mul(0x9E3779B97F4A7C15));
                        z ^= z >> 30;
                        z = z.wrapping_mul(0xBF58476D1CE4E5B9);
                        z ^= z >> 27;
                        z = z.wrapping_mul(0x94D049BB133111EB);
                        z ^= z >> 31;
                        (z as usize) % R
                    }
                }
            };
            let sub_mod = |a: &BigUint, b: &BigUint, m: &BigUint| -> BigUint {
                if a >= b {
                    (a - b) % m
                } else {
                    let diff = (b - a) % m;
                    if diff.is_zero() { BigUint::zero() } else { m - diff }
                }
            };

            // Random initial (a, b).
            let a0 = (&mut wrng).gen_biguint_below(n);
            let b0 = (&mut wrng).gen_biguint_below(n);
            let mut pt = g_pt
                .scalar_mul(&a0, a_fe)
                .add(&h_pt.scalar_mul(&b0, a_fe), a_fe);
            let mut a_acc = a0;
            let mut b_acc = b0;
            let mut local: u64 = 0;

            while local < per_walker_cap {
                if local & 0x3FF == 0 && done.load(Ordering::Relaxed) {
                    break;
                }
                let bk = bucket(&pt);
                let (add_pt, add_a, add_b) = &adders[bk];
                pt = pt.add(add_pt, a_fe);
                a_acc = (&a_acc + add_a) % n;
                b_acc = (&b_acc + add_b) % n;
                local += 1;

                let x_low = match &pt {
                    Point::Affine { x, .. } => x.value.iter_u64_digits().next().unwrap_or(0),
                    Point::Infinity => continue,
                };
                if (x_low & dp_mask) == 0 {
                    let mut tbl = dp_table.lock().unwrap();
                    if let Some((prev_a, prev_b)) = tbl.get(&x_low).cloned() {
                        drop(tbl);
                        let lhs = sub_mod(&a_acc, &prev_a, n);
                        let rhs = sub_mod(&prev_b, &b_acc, n);
                        if rhs.is_zero() {
                            continue;
                        }
                        let g_gcd = rhs.gcd(n);
                        if g_gcd.is_one() {
                            if let Some(inv) = mod_inverse(&rhs, n) {
                                let x_val = (&lhs * &inv) % n;
                                let test = g_pt.scalar_mul(&x_val, a_fe);
                                if test == *h_pt {
                                    let mut rg = result.lock().unwrap();
                                    if rg.is_none() {
                                        *rg = Some(x_val);
                                        done.store(true, Ordering::Relaxed);
                                    }
                                    break;
                                }
                            }
                        } else if &lhs % &g_gcd == BigUint::zero() && g_gcd.bits() <= 16 {
                            let m_red = n / &g_gcd;
                            let lhs_red = &lhs / &g_gcd;
                            let rhs_red = &rhs / &g_gcd;
                            if let Some(inv) = mod_inverse(&rhs_red, &m_red) {
                                let x_base = (&lhs_red * &inv) % &m_red;
                                let g_u: u64 = g_gcd.iter_u64_digits().next().unwrap_or(0);
                                let mut x_cand = x_base;
                                let mut found = false;
                                for _ in 0..g_u {
                                    let test = g_pt.scalar_mul(&x_cand, a_fe);
                                    if test == *h_pt {
                                        let mut rg = result.lock().unwrap();
                                        if rg.is_none() {
                                            *rg = Some(x_cand);
                                            done.store(true, Ordering::Relaxed);
                                        }
                                        found = true;
                                        break;
                                    }
                                    x_cand = (&x_cand + &m_red) % n;
                                }
                                if found {
                                    break;
                                }
                            }
                        }
                    } else {
                        tbl.insert(x_low, (a_acc.clone(), b_acc.clone()));
                    }
                }
            }
            total_iters.fetch_add(local, Ordering::Relaxed);
        }));
    }

    for h in handles {
        let _ = h.join();
    }

    let total = total_iters.load(Ordering::Relaxed);
    let final_x = result.lock().unwrap().take();
    match final_x {
        Some(x) => (Some(RhoSolution { x, iterations: total }), total),
        None => (None, max_iters),
    }
}

/// **Multi-target GLS rho** (round-7 upgrade).  Solves `m` discrete
/// logs `h_i = d_i · g` in the same group with a *shared* walk:
/// each step updates `(a, b_0, …, b_{m-1})` such that the running
/// element equals `a·g + Σ b_i·h_i`.  A DP collision between two
/// walkers gives the equation
/// ```text
///   Σ_i (b_i − b'_i) · d_i ≡ a' − a   (mod n)
/// ```
/// `m` linearly-independent equations let us solve for all `d_i`
/// at once via Gaussian elimination mod n.  Galbraith-Lin-Scott
/// 2011 prove total cost `O(√(n·m))`, so per-target it is
/// `√(n/m)` — a `√m` speedup over single-target rho.
///
/// Returns `Some(d_0, …, d_{m-1})` on success.
pub fn multi_target_dp_rho(
    g: &Point,
    targets: &[Point],
    n: &BigUint,
    a_fe: &crate::ecc::field::FieldElement,
    max_iters: u64,
    seed: u64,
) -> (Option<Vec<BigUint>>, u64) {
    use crate::utils::mod_inverse;
    use num_bigint::RandBigInt;
    use num_integer::Integer;
    use rand::rngs::StdRng;
    use rand::SeedableRng;
    use std::collections::HashMap;
    use std::sync::atomic::{AtomicBool, AtomicU64, Ordering};
    use std::sync::{Arc, Mutex};

    let m = targets.len();
    if m == 0 {
        return (None, 0);
    }
    const K: usize = 4;
    const R: usize = 20;

    // Precompute R adders: each is m_j = a_j·g + Σ b_{j,i}·h_i
    let mut prng = StdRng::seed_from_u64(seed ^ 0xCAFEFACE_FEEDF00D);
    let mut adders: Vec<(Point, BigUint, Vec<BigUint>)> = Vec::with_capacity(R);
    for _ in 0..R {
        let aj = (&mut prng).gen_biguint_below(n);
        let bj: Vec<BigUint> = (0..m).map(|_| (&mut prng).gen_biguint_below(n)).collect();
        let mut pt = g.scalar_mul(&aj, a_fe);
        for i in 0..m {
            pt = pt.add(&targets[i].scalar_mul(&bj[i], a_fe), a_fe);
        }
        adders.push((pt, aj, bj));
    }
    let adders = Arc::new(adders);

    // DP density: roughly bits(n)/4 for medium scales, but with a
    // small minimum so 16-bit toy tests actually emit a few DPs.
    let dp_bits = (((n.bits() as u32) / 4).max(4)).min(28) as u64;
    let dp_mask = (1u64 << dp_bits) - 1;

    // Shared state.  DP table maps point-key → (a, b_0, …, b_{m-1}).
    let dp_table: Arc<Mutex<HashMap<u64, Vec<BigUint>>>> =
        Arc::new(Mutex::new(HashMap::new()));
    // Incremental Gaussian elimination state.  `pivots[col] =
    // Some(row)` if we have a pivot for column `col`.  Rows are
    // stored in `rows`, each as (coeffs[0..m], rhs).
    let rref: Arc<Mutex<RrefState>> = Arc::new(Mutex::new(RrefState::new(m)));
    let result: Arc<Mutex<Option<Vec<BigUint>>>> = Arc::new(Mutex::new(None));
    let total_iters = Arc::new(AtomicU64::new(0));
    let done = Arc::new(AtomicBool::new(false));

    let per_walker_cap = max_iters / K as u64;
    let n_arc = Arc::new(n.clone());
    let g_arc = Arc::new(g.clone());
    let targets_arc: Arc<Vec<Point>> = Arc::new(targets.to_vec());
    let a_fe_arc = Arc::new(a_fe.clone());

    let mut handles = Vec::with_capacity(K);
    for wid in 0..K {
        let adders = Arc::clone(&adders);
        let dp_table = Arc::clone(&dp_table);
        let rref = Arc::clone(&rref);
        let result = Arc::clone(&result);
        let total_iters = Arc::clone(&total_iters);
        let done = Arc::clone(&done);
        let n_arc = Arc::clone(&n_arc);
        let g_arc = Arc::clone(&g_arc);
        let targets_arc = Arc::clone(&targets_arc);
        let a_fe_arc = Arc::clone(&a_fe_arc);

        handles.push(std::thread::spawn(move || {
            let n = &*n_arc;
            let g_pt = &*g_arc;
            let targets = &*targets_arc;
            let a_fe = &*a_fe_arc;
            let mut wrng = StdRng::seed_from_u64(
                seed.wrapping_add((wid as u64).wrapping_mul(0xC6BC279692B5C323)),
            );
            let bucket = |pt: &Point| -> usize {
                match pt {
                    Point::Infinity => 0,
                    Point::Affine { x, y } => {
                        let xv = x.value.iter_u64_digits().next().unwrap_or(0);
                        let yv = y.value.iter_u64_digits().next().unwrap_or(0);
                        let mut z = xv.wrapping_add(yv.wrapping_mul(0x9E3779B97F4A7C15));
                        z ^= z >> 30;
                        z = z.wrapping_mul(0xBF58476D1CE4E5B9);
                        z ^= z >> 27;
                        z = z.wrapping_mul(0x94D049BB133111EB);
                        z ^= z >> 31;
                        (z as usize) % R
                    }
                }
            };
            let sub_mod = |a: &BigUint, b: &BigUint, m: &BigUint| -> BigUint {
                if a >= b {
                    (a - b) % m
                } else {
                    let diff = (b - a) % m;
                    if diff.is_zero() { BigUint::zero() } else { m - diff }
                }
            };

            // Random initial state.
            let a0 = (&mut wrng).gen_biguint_below(n);
            let bs0: Vec<BigUint> = (0..m)
                .map(|_| (&mut wrng).gen_biguint_below(n))
                .collect();
            let mut pt = g_pt.scalar_mul(&a0, a_fe);
            for i in 0..m {
                pt = pt.add(&targets[i].scalar_mul(&bs0[i], a_fe), a_fe);
            }
            let mut a_acc = a0;
            let mut bs_acc = bs0;
            let mut local: u64 = 0;

            while local < per_walker_cap {
                if local & 0x3FF == 0 && done.load(Ordering::Relaxed) {
                    break;
                }
                let bk = bucket(&pt);
                let (add_pt, add_a, add_bs) = &adders[bk];
                pt = pt.add(add_pt, a_fe);
                a_acc = (&a_acc + add_a) % n;
                for i in 0..m {
                    bs_acc[i] = (&bs_acc[i] + &add_bs[i]) % n;
                }
                local += 1;

                let (x_low, key) = match &pt {
                    Point::Affine { x, y } => {
                        let xv = x.value.iter_u64_digits().next().unwrap_or(0);
                        let yv = y.value.iter_u64_digits().next().unwrap_or(0);
                        // Distinguishedness uses x; the table key
                        // mixes x and y so P and −P don't collide.
                        let key = xv.wrapping_mul(0x9E3779B97F4A7C15).wrapping_add(yv);
                        (xv, key)
                    }
                    Point::Infinity => continue,
                };
                if (x_low & dp_mask) == 0 {
                    let mut tbl = dp_table.lock().unwrap();
                    let mut entry: Vec<BigUint> = Vec::with_capacity(m + 1);
                    entry.push(a_acc.clone());
                    for i in 0..m {
                        entry.push(bs_acc[i].clone());
                    }
                    if let Some(prev) = tbl.get(&key).cloned() {
                        drop(tbl);
                        // Equation: Σ (b_i − b'_i) d_i ≡ (a' − a) (mod n)
                        let coeffs: Vec<BigUint> = (0..m)
                            .map(|i| sub_mod(&bs_acc[i], &prev[i + 1], n))
                            .collect();
                        let rhs = sub_mod(&prev[0], &a_acc, n);
                        let mut g_state = rref.lock().unwrap();
                        let now_solved = g_state.add_equation(&coeffs, &rhs, n);
                        if now_solved {
                            let sol = g_state.read_solution();
                            drop(g_state);
                            // Verify.
                            let mut ok = true;
                            for (i, dval) in sol.iter().enumerate() {
                                let test = g_pt.scalar_mul(dval, a_fe);
                                if test != targets[i] {
                                    ok = false;
                                    break;
                                }
                            }
                            if ok {
                                let mut rg = result.lock().unwrap();
                                if rg.is_none() {
                                    *rg = Some(sol);
                                    done.store(true, Ordering::Relaxed);
                                }
                                break;
                            }
                            // Verification failed: the recovered tuple
                            // is wrong (probably hit a non-invertible
                            // pivot on a row that GE accepted but is
                            // mod a divisor of n).  Reset the RREF
                            // state so further collisions can build a
                            // fresh system.
                            let mut g_state = rref.lock().unwrap();
                            *g_state = RrefState::new(m);
                        }
                    } else {
                        tbl.insert(key, entry);
                    }
                }
            }
            total_iters.fetch_add(local, Ordering::Relaxed);
        }));
    }

    for h in handles {
        let _ = h.join();
    }

    let total = total_iters.load(Ordering::Relaxed);
    let final_x = result.lock().unwrap().take();
    (final_x, total)
}

/// Trial-divide `n` up to `bound`.  Returns `(small_factors, residual)`
/// where each `small_factors[i] = (q, e)` is a prime power dividing
/// `n`, and `residual = n / Π q^e` is the cofactor (1 if `n` is
/// `bound`-smooth, otherwise a residue that may be prime or a
/// product of large primes).
fn factor_smooth(n: u64, bound: u64) -> (Vec<(u64, u32)>, u64) {
    let mut factors = Vec::new();
    let mut rem = n;
    let mut q = 2u64;
    while q <= bound && q * q <= rem {
        if rem % q == 0 {
            let mut e = 0u32;
            while rem % q == 0 {
                rem /= q;
                e += 1;
            }
            factors.push((q, e));
        }
        q += if q == 2 { 1 } else { 2 };
    }
    // If the residual is prime ≤ bound, account for it.
    if rem > 1 && rem <= bound {
        factors.push((rem, 1));
        rem = 1;
    }
    (factors, rem)
}

/// **Pohlig-Hellman + multi-target GLS** (round-8).  Factors `n`
/// into smooth + residual; for each prime-power factor `q^e`,
/// lifts `g` and the targets into the order-`q^e` subgroup via
/// scalar multiplication by `n/q^e`, runs multi-target GLS rho
/// there (which never stalls because the subgroup order is a
/// prime power), recovers `d_i mod q^e`, and CRT-combines all
/// the residues to recover `d_i mod n`.
///
/// For the typical curve in our sweep, `n = 2 · p` with `p` a
/// large odd prime.  This routine handles the `q = 2` factor
/// trivially (one curve operation per target), then runs GLS
/// in the order-`p` subgroup where the rank-stalling problem
/// of round 7 does not occur (every non-zero element is
/// invertible modulo a prime).
pub fn pohlig_multi_target_rho(
    g: &Point,
    targets: &[Point],
    n: &BigUint,
    a_fe: &crate::ecc::field::FieldElement,
    max_iters_per_subgroup: u64,
    seed: u64,
) -> (Option<Vec<BigUint>>, u64) {
    use crate::cryptanalysis::pohlig_hellman::crt_combine;
    use num_traits::One;

    let m = targets.len();
    if m == 0 {
        return (None, 0);
    }
    let n_u64: u64 = match n.iter_u64_digits().next() {
        Some(v) if n.bits() <= 64 => v,
        _ => return multi_target_dp_rho(g, targets, n, a_fe, max_iters_per_subgroup, seed),
    };
    if n.bits() > 63 {
        return multi_target_dp_rho(g, targets, n, a_fe, max_iters_per_subgroup, seed);
    }

    // Smooth-bound: factor out everything up to 2^20.  Larger
    // residuals are handled by a single GLS run in their subgroup.
    let (small, residual) = factor_smooth(n_u64, 1 << 20);
    let mut all_factors: Vec<(u64, u32)> = small.clone();
    if residual > 1 {
        all_factors.push((residual, 1));
    }
    if all_factors.len() < 2 {
        // n itself is a prime power: just run GLS directly.
        return multi_target_dp_rho(g, targets, n, a_fe, max_iters_per_subgroup, seed);
    }

    // For each (q, e): lift base/targets to order-q^e subgroup
    // by scalar-multiplying by n / q^e, then run GLS rho there.
    let mut residues_per_target: Vec<Vec<(BigUint, BigUint)>> =
        vec![Vec::new(); m];
    let mut total_iters: u64 = 0;
    for (q, e) in &all_factors {
        let mut qe: u64 = 1;
        for _ in 0..*e { qe *= q; }
        let qe_big = BigUint::from(qe);
        let cofactor = BigUint::from(n_u64 / qe);
        let g_sub = g.scalar_mul(&cofactor, a_fe);
        let h_sub: Vec<Point> = targets
            .iter()
            .map(|h| h.scalar_mul(&cofactor, a_fe))
            .collect();
        if matches!(g_sub, Point::Infinity) {
            // Degenerate: g lifts to identity, skip.  d_i mod q^e
            // is unconstrained, default to 0.
            for i in 0..m {
                residues_per_target[i].push((qe_big.clone(), BigUint::zero()));
            }
            continue;
        }
        // For q = 2, e = 1: brute-force check h^{n/2} = O.
        if *q == 2 && *e == 1 {
            for i in 0..m {
                let r = if matches!(h_sub[i], Point::Infinity) {
                    BigUint::zero()
                } else {
                    BigUint::one()
                };
                residues_per_target[i].push((qe_big.clone(), r));
            }
            continue;
        }
        // Try multi-target GLS first.  It works for prime q^e
        // because invertibility mod a prime gives RrefState a clean
        // run.  For prime-power orders (e > 1) and certain awkward
        // seeds the rank can stall — fall back to per-target single
        // rho in the same subgroup so smooth-n curves still recover.
        let (sol, iters) = multi_target_dp_rho(
            &g_sub,
            &h_sub,
            &qe_big,
            a_fe,
            max_iters_per_subgroup,
            seed.wrapping_add(*q),
        );
        total_iters += iters;
        match sol {
            Some(s) => {
                for i in 0..m {
                    residues_per_target[i].push((qe_big.clone(), s[i].clone()));
                }
            }
            None => {
                // Per-target fallback.
                for i in 0..m {
                    let (fb_sol, fb_iters) = dp_parallel_rho_on_curve(
                        &g_sub,
                        &h_sub[i],
                        &qe_big,
                        a_fe,
                        max_iters_per_subgroup,
                        seed.wrapping_add(*q).wrapping_mul(i as u64 + 1),
                    );
                    total_iters += fb_iters;
                    let r = match fb_sol {
                        Some(s) => s.x,
                        None => return (None, total_iters),
                    };
                    residues_per_target[i].push((qe_big.clone(), r));
                }
            }
        }
    }

    // CRT each target's residues to recover d_i mod n.
    let mut sol = Vec::with_capacity(m);
    for i in 0..m {
        let r = crt_combine(&residues_per_target[i]);
        sol.push(r.unwrap_or(BigUint::zero()));
    }
    (Some(sol), total_iters)
}

/// **Incremental Gaussian elimination state** maintained as the
/// multi-target rho stream of equations arrives.  Stores rows in
/// reduced echelon form: `rows[col]` is the row pivoted at column
/// `col` (`None` if no such pivot yet).  Adding a new equation runs
/// elimination against existing pivots in O(m²) time, then either
/// discards (reduced to zero), pivots a new column, or returns
/// without progress.  Once all `m` pivots exist, `read_solution()`
/// extracts `(d_0, …, d_{m-1})`.
struct RrefState {
    /// Rows in echelon form.  Each row is (coeffs[0..m], rhs).
    /// `rows[k] = Some((coeffs, rhs))` iff a pivot was found in
    /// column k; that row has 1 in column k and 0 in earlier
    /// pivoted columns.
    rows: Vec<Option<(Vec<BigUint>, BigUint)>>,
    m: usize,
    rank: usize,
}

impl RrefState {
    fn new(m: usize) -> Self {
        Self {
            rows: vec![None; m],
            m,
            rank: 0,
        }
    }

    /// Reduce `(coeffs, rhs)` against existing pivots; if a non-zero
    /// row remains, pivot it into the matrix.  Returns true iff the
    /// matrix now has rank `m` (i.e. the system is solvable).
    fn add_equation(
        &mut self,
        coeffs_in: &[BigUint],
        rhs_in: &BigUint,
        n: &BigUint,
    ) -> bool {
        use crate::utils::mod_inverse;
        if self.rank == self.m {
            return true;
        }
        let sub_mod = |a: &BigUint, b: &BigUint, m: &BigUint| -> BigUint {
            if a >= b {
                (a - b) % m
            } else {
                let diff = (b - a) % m;
                if diff.is_zero() { BigUint::zero() } else { m - diff }
            }
        };

        let mut coeffs: Vec<BigUint> = coeffs_in.to_vec();
        let mut rhs: BigUint = rhs_in.clone();

        // Step 1: reduce against existing pivots.
        for col in 0..self.m {
            if let Some((pivot_coeffs, pivot_rhs)) = &self.rows[col] {
                if !coeffs[col].is_zero() {
                    let factor = coeffs[col].clone();
                    for c in 0..self.m {
                        let t = (&pivot_coeffs[c] * &factor) % n;
                        coeffs[c] = sub_mod(&coeffs[c], &t, n);
                    }
                    let t = (pivot_rhs * &factor) % n;
                    rhs = sub_mod(&rhs, &t, n);
                }
            }
        }

        // Step 2: find first non-zero column with invertible entry.
        let mut pivot_col: Option<usize> = None;
        for col in 0..self.m {
            if self.rows[col].is_some() {
                continue;
            }
            if !coeffs[col].is_zero() && mod_inverse(&coeffs[col], n).is_some() {
                pivot_col = Some(col);
                break;
            }
        }
        let col = match pivot_col {
            Some(c) => c,
            None => return self.rank == self.m, // no new pivot
        };

        // Step 3: normalise.
        let inv = mod_inverse(&coeffs[col], n).unwrap();
        for c in 0..self.m {
            coeffs[c] = (&coeffs[c] * &inv) % n;
        }
        rhs = (&rhs * &inv) % n;

        // Step 4: back-substitute into earlier pivots so they get 0
        // in column `col`.
        for earlier in 0..self.m {
            if earlier == col {
                continue;
            }
            if let Some((p_coeffs, p_rhs)) = &mut self.rows[earlier] {
                if !p_coeffs[col].is_zero() {
                    let factor = p_coeffs[col].clone();
                    let mut new_p_coeffs = p_coeffs.clone();
                    for c in 0..self.m {
                        let t = (&coeffs[c] * &factor) % n;
                        new_p_coeffs[c] = sub_mod(&new_p_coeffs[c], &t, n);
                    }
                    let t = (&rhs * &factor) % n;
                    let new_p_rhs = sub_mod(p_rhs, &t, n);
                    *p_coeffs = new_p_coeffs;
                    *p_rhs = new_p_rhs;
                }
            }
        }

        self.rows[col] = Some((coeffs, rhs));
        self.rank += 1;
        #[cfg(feature = "rho_debug")]
        eprintln!("rref: pivot at col {}, rank now {}/{}", col, self.rank, self.m);
        self.rank == self.m
    }

    fn read_solution(&self) -> Vec<BigUint> {
        let mut sol = vec![BigUint::zero(); self.m];
        for col in 0..self.m {
            if let Some((_, rhs)) = &self.rows[col] {
                sol[col] = rhs.clone();
            }
        }
        sol
    }
}

/// Solve `A · x ≡ c (mod n)` by Gaussian elimination, requiring
/// every pivot to be invertible mod `n`.  Returns `None` if the
/// system is under-determined or hits a non-invertible pivot
/// (which happens with probability ~`1 − φ(n)/n` per equation,
/// well below 1% when `n` is near-prime).
///
/// Superseded by the incremental [`RrefState`] used in
/// `multi_target_dp_rho`; kept for reference / future re-use.
#[allow(dead_code)]
fn solve_mod_n(
    equations: &[(Vec<BigUint>, BigUint)],
    n: &BigUint,
    m: usize,
) -> Option<Vec<BigUint>> {
    use crate::utils::mod_inverse;

    if equations.len() < m {
        return None;
    }
    // Working matrix: rows of [coeffs | rhs].
    let mut mat: Vec<Vec<BigUint>> = equations
        .iter()
        .map(|(row, rhs)| {
            let mut r = row.clone();
            r.push(rhs.clone());
            r
        })
        .collect();

    let sub_mod = |a: &BigUint, b: &BigUint, m: &BigUint| -> BigUint {
        if a >= b {
            (a - b) % m
        } else {
            let diff = (b - a) % m;
            if diff.is_zero() { BigUint::zero() } else { m - diff }
        }
    };

    let mut pivot_row_for_col: Vec<Option<usize>> = vec![None; m];

    for col in 0..m {
        // Find a not-yet-used row with mat[r][col] invertible mod n.
        let mut chosen: Option<usize> = None;
        for r in 0..mat.len() {
            if pivot_row_for_col.iter().any(|&pr| pr == Some(r)) {
                continue;
            }
            if !mat[r][col].is_zero() {
                if mod_inverse(&mat[r][col], n).is_some() {
                    chosen = Some(r);
                    break;
                }
            }
        }
        let r = chosen?;
        pivot_row_for_col[col] = Some(r);
        let inv = mod_inverse(&mat[r][col], n).unwrap();
        for c in 0..=m {
            mat[r][c] = (&mat[r][c] * &inv) % n;
        }
        // Eliminate the column from all OTHER rows.
        for other in 0..mat.len() {
            if other == r || mat[other][col].is_zero() {
                continue;
            }
            let factor = mat[other][col].clone();
            for c in 0..=m {
                let term = (&mat[r][c] * &factor) % n;
                mat[other][c] = sub_mod(&mat[other][c], &term, n);
            }
        }
    }

    let mut sol = vec![BigUint::zero(); m];
    for col in 0..m {
        let r = pivot_row_for_col[col]?;
        sol[col] = mat[r][m].clone();
    }
    Some(sol)
}

/// Top-level attack-suite runner.  Computes every feasibility
/// detector and runs a single rho attempt with `rho_max_iters`.
pub fn run_attack_suite(curve: &SmallCurve, rho_max_iters: u64, seed: u64) -> AttackReport {
    let cm = cm_discriminant(curve);
    let n = cm.order.unsigned_abs();
    let emb = embedding_degree(curve.p, n, 32);
    let mov = emb.map(|k| k <= 6).unwrap_or(false);
    let smart = cm.anomalous;
    let glv = glv_endomorphism_available(curve);

    let (sol, iters) = if BigUint::zero() < BigUint::from(n) {
        rho_on_curve(curve, rho_max_iters, seed)
    } else {
        (None, 0)
    };

    AttackReport {
        curve: (curve.p, curve.a, curve.b),
        cm,
        rho_iters: Some(iters),
        rho_success: sol.is_some(),
        embedding_degree: emb,
        mov_feasible: mov,
        smart_applies: smart,
        glv_speedup_available: glv,
    }
}

