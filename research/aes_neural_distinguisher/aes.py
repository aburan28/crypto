RCON = np.array([0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36], dtype=np.uint8)


def _xtime(x: np.ndarray) -> np.ndarray:
    return ((x << np.uint8(1)) ^ (((x >> np.uint8(7)) & np.uint8(1)) * np.uint8(0x1b))).astype(np.uint8)


def bytes_to_state(b: np.ndarray) -> np.ndarray:
    """(B, 16) -> (B, 4, 4) column-major: state[:, r, c] = b[:, 4c + r]."""
    return b.reshape(-1, 4, 4).transpose(0, 2, 1).copy()


def state_to_bytes(s: np.ndarray) -> np.ndarray:
    """Inverse of bytes_to_state."""
    return s.transpose(0, 2, 1).reshape(-1, 16).copy()


def key_schedule(key: np.ndarray, rounds: int) -> np.ndarray:
    """key: (B, 16) uint8. Returns round keys as (B, rounds+1, 4, 4)."""
    B = key.shape[0]
    n_words = 4 * (rounds + 1)
    W = np.zeros((B, n_words, 4), dtype=np.uint8)
    W[:, :4] = key.reshape(B, 4, 4)  # W[i] = key bytes 4i..4i+3
    for i in range(4, n_words):
        t = W[:, i - 1].copy()
        if i % 4 == 0:
            # RotWord
            t = np.roll(t, -1, axis=1)
            # SubWord
            t = SBOX[t]
            # XOR Rcon (first byte only)
            t[:, 0] ^= RCON[i // 4 - 1]
        W[:, i] = W[:, i - 4] ^ t
    # Pack into round keys with state layout (row, col)
    rk = W.reshape(B, rounds + 1, 4, 4)  # axis 2 = word index within round = column
    rk = rk.transpose(0, 1, 3, 2)  # (B, rounds+1, row, col)
    return np.ascontiguousarray(rk)


def sub_bytes(s: np.ndarray) -> np.ndarray:
    return SBOX[s]


def shift_rows(s: np.ndarray) -> np.ndarray:
    out = np.empty_like(s)
    out[:, 0] = s[:, 0]
    out[:, 1] = np.roll(s[:, 1], -1, axis=1)
    out[:, 2] = np.roll(s[:, 2], -2, axis=1)
    out[:, 3] = np.roll(s[:, 3], -3, axis=1)
    return out


def mix_columns(s: np.ndarray) -> np.ndarray:
    s2 = _xtime(s)
    s3 = s2 ^ s
    out = np.empty_like(s)
    out[:, 0] = s2[:, 0] ^ s3[:, 1] ^ s[:, 2]  ^ s[:, 3]
    out[:, 1] = s[:, 0]  ^ s2[:, 1] ^ s3[:, 2] ^ s[:, 3]
    out[:, 2] = s[:, 0]  ^ s[:, 1]  ^ s2[:, 2] ^ s3[:, 3]
    out[:, 3] = s3[:, 0] ^ s[:, 1]  ^ s[:, 2]  ^ s2[:, 3]
    return out


def encrypt(pt: np.ndarray, key: np.ndarray, rounds: int) -> np.ndarray:
    """pt, key: (B, 16) uint8. Returns ciphertext (B, 16). Last round omits MixColumns."""
    assert 1 <= rounds <= 10
    rk = key_schedule(key, rounds)
    s = bytes_to_state(pt)
    s ^= rk[:, 0]
    for r in range(1, rounds):
        s = sub_bytes(s)
        s = shift_rows(s)
        s = mix_columns(s)
        s ^= rk[:, r]
    # Last round: no MixColumns
    s = sub_bytes(s)
    s = shift_rows(s)
    s ^= rk[:, rounds]
    return state_to_bytes(s)


def _self_test() -> None:
    # FIPS-197 Appendix B test vector for full 10-round AES.
    pt = np.frombuffer(bytes.fromhex("3243f6a8885a308d313198a2e0370734"), dtype=np.uint8).reshape(1, 16)
    key = np.frombuffer(bytes.fromhex("2b7e151628aed2a6abf7158809cf4f3c"), dtype=np.uint8).reshape(1, 16)
    expected = bytes.fromhex("3925841d02dc09fbdc118597196a0b32")
    got = encrypt(pt, key, rounds=10)[0].tobytes()
    assert got == expected, f"AES test vector mismatch: {got.hex()} != {expected.hex()}"
    print("aes.py self-test passed")


if __name__ == "__main__":
    _self_test()
